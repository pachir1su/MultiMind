REFINEMENT_TEMPLATE = """사용자의 프롬프트를 각 Worker LLM이 최고의 응답을 생성할 수 있도록 최적화하세요.

핵심 규칙:
- 사용자의 원래 의도를 절대 변경하지 마세요
- 짧은 프롬프트(단어, 이름, 주제명 등)는 해당 대상에 대한 정보·설명 요청으로 해석하세요
- 사용자가 명시적으로 요청하지 않은 페르소나·역할극·시스템 프롬프트를 절대 추가하지 마세요
- 프롬프트의 의미를 보존하면서, 각 Worker의 특성에 맞게 표현·상세도·형식만 조정하세요
- 설명·인사·사족 없이 JSON만 출력하세요

프롬프트: {user_prompt}
Workers: {worker_list}

정확히 이 형식으로 출력 (JSON 외 텍스트 절대 금지):
{json_template}"""

